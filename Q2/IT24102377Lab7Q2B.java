public class IT24102377Lab7Q2B {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i + " - ");
            for (int j = 1; j <= i; j++) {
                System.out.println("* ");
            }
            System.out.println();
        }
    }
}